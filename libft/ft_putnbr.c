#include "libft.h"

void		ft_putnbr(int n)
{
	char	s[11];
	int		i;
	int		flag;

	flag = (n < 0) ? (-1) : 1;
	i = 0;
	if (n == 0)
		write(1, "0", 1);
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		ft_putnbr(147483648);
		return ;
	}
	n = n * flag;
	while (n > 0)
	{
		s[i] = (n % 10) + 48;
		n = n / 10;
		i++;
	}
	if (flag == -1)
		write(1, "-", 1);
	while (i-- > 0)
		write(1, &s[i], 1);
}
