#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	i1;

	i = 0;
	i1 = ft_strlen(s1);
	while (i < n && s2[i])
	{
		s1[i1] = s2[i];
		i1++;
		i++;
	}
	s1[i1] = '\0';
	return (s1);
}
